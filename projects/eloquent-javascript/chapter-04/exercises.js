// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
  let rev = []
  for (let index = array.length-1; index >= 0; index--){
    rev.push(array[index])
  }
  return rev
}

