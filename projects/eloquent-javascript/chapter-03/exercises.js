// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a, b) {

  if (a > b){
    return b
  } else if (a < b){
    return a
  } else {
    return a
  }

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
    return num % 2 === 0
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, cha) {
   let count = 0
   for (let index = 0; index < string.length; index++){
    if (string[index] === cha){
      count +=1
    }
   }
   return count
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count = 0
  for (let index = 0; index < string.length; index++){
   if (string[index] === 'B'){
     count +=1
   }
  }
  return count
}

